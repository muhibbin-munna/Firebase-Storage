package com.muhibbin.firebasestoragedemo;

public class Upload {
    public String name,url;

    public Upload() {
    }

    public Upload(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }


    public String getUrl() {
        return url;
    }

}
